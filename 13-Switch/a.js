let valor = 10
switch (valor) {
    case 1:
        console.log('Valor é igual a 1');
        break;
    case 2:
        console.log('Valor é igual a 2');
        break;
    case 3:
        console.log('Valor é igual a 3');
        break;
    default:
        console.log('Nenhuma das opções anteriores...')
        break;
}